public class ZohocorpPattern {
    public static void main(String[] args) {
        String[] lines = {
            "ZOHO",
            "CORP",
            "ORAT",
            "IONS"
        };

        int i = 0;
        do {
            System.out.println(lines[i]);
            i++;
        } while (i < lines.length);
    }
}
